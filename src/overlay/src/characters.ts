import type { CharacterId } from '../../shared/ipc-types'

// Each character defined as pixel art rows using palette indices.
// Palette: 0=transparent, 1=skin/primary, 2=dark/accent, 3=highlight, 4=secondary, 5=outline
// Each frame is an array of rows (top→bottom), each row is a string of palette indices.
// 10×16 pixels per frame, 2 walk frames, 1 idle frame

export interface PixelFrame {
  rows: string[]
}

export interface CharacterDef {
  name: string
  palette: string[] // index → CSS color
  walk: [PixelFrame, PixelFrame]
  idle: PixelFrame
  talk: PixelFrame
  emoji: string // fallback emoji
}

// ─── Helper ──────────────────────────────────────────────────────────────────
function f(rows: string[]): PixelFrame {
  return { rows }
}

